def navigate_trail(map):
    moves = {        
        (0, 1): "R",
        (1, 0): "D",
        (0, -1): "L",
        (-1, 0): "U"
    }

    row, col = 0, 0
    for i in range(len(map)):
        for j in range(len(map[0])):
            if map[i][j] == "C":
                    row, col = i, j

    def is_valid_move(next_row, next_col):
        return next_row >= 0 and next_col >= 0 and next_row < len(map) and next_col < len(map[0])

    def print_map():
        for line in map_li:
            print(line)
        print()

    map_li = []
    for line in map:
        map_li.append(list(line))
    print_map()

    result = []
    count = 0
    while map_li[row][col] != "G" and count < 100:
        for move in moves:
            next_row = row + move[0]
            next_col = col + move[1]
            if not is_valid_move(next_row, next_col):
                continue
            if map_li[next_row][next_col] in ("G", "T"):
                result.append(moves[move])
                map_li[row][col] = "-"
                if map_li[next_row][next_col] == "T":
                    map_li[next_row][next_col] = "C"
                    print_map()
                row, col = next_row, next_col
                break
        count += 1
    return ''.join(result)

